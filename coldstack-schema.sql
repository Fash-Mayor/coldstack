CREATE TABLE IF NOT EXISTS public.trip_photos (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  trip_id uuid NOT NULL REFERENCES public.trips(id) ON DELETE CASCADE,

  start_photo_url text,
  end_photo_url text,

  start_captured_at timestamp,
  end_captured_at timestamp,

  created_at timestamptz NOT NULL DEFAULT now()
);

-- telemetry: high-frequency inserts
CREATE TABLE IF NOT EXISTS public.telemetry (
  id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  trip_id uuid NOT NULL REFERENCES public.trips(id) ON DELETE CASCADE,

  temperature decimal,
  coords geography(POINT, 4326),

  logged_at timestamptz NOT NULL DEFAULT now(),

  created_at timestamptz NOT NULL DEFAULT now(),

  -- Constraint to guard bad timestamps (helps keep insert stream sane)
  CONSTRAINT telemetry_logged_at_reasonable CHECK (
    logged_at <= (now() + interval '5 minutes')
  )
);

-- POC certificates
CREATE TABLE IF NOT EXISTS public.poc_certificates (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  trip_id uuid NOT NULL REFERENCES public.trips(id) ON DELETE CASCADE,

  pdf_url text NOT NULL,
  generated_at timestamp NOT NULL DEFAULT now(),
  verified_by_receiver boolean NOT NULL DEFAULT false,

  created_at timestamptz NOT NULL DEFAULT now()
);

-- KPIs
CREATE TABLE IF NOT EXISTS public.kpis (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  entity_id uuid NOT NULL,

  avg_temp_deviation decimal,
  on_time_trip_rate decimal,
  total_trips int,

  created_at timestamptz NOT NULL DEFAULT now()
);


-- 1) Extensions, Enums & Types
CREATE EXTENSION IF NOT EXISTS postgis;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'user_role') THEN
    CREATE TYPE user_role AS ENUM ('admin', 'carrier', 'shipper', 'consignee');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'trip_status') THEN
    CREATE TYPE trip_status AS ENUM (
      'pending', 'assigned', 'en_route', 'on_trip', 'completed', 'cancelled'
    );
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'logger_status') THEN
    CREATE TYPE logger_status AS ENUM ('warehouse', 'maintenance', 'active', 'ready');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'stack_status') THEN
    CREATE TYPE stack_status AS ENUM ('warehouse', 'cleaning', 'active', 'ready', 'maintenance');
  END IF;
END $$;


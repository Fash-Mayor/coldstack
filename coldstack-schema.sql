-- profiles: extends auth.users (1:1 by id)
CREATE TABLE IF NOT EXISTS public.profiles (
  id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name text,
  role user_role NOT NULL DEFAULT 'shipper',
  email text,
  phone text,
  onboarding_completed boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- loggers
CREATE TABLE IF NOT EXISTS public.loggers (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  serial text UNIQUE NOT NULL,
  status logger_status NOT NULL DEFAULT 'ready',
  battery_level int,
  last_active timestamp,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- stacks
CREATE TABLE IF NOT EXISTS public.stacks (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  serial text UNIQUE NOT NULL,
  status stack_status NOT NULL DEFAULT 'ready',
  current_logger_id uuid REFERENCES public.loggers(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- locations (Address book)
CREATE TABLE IF NOT EXISTS public.locations (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  owner_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  label text NOT NULL,
  address_text text NOT NULL,
  coords geography(POINT, 4326),
  created_at timestamptz NOT NULL DEFAULT now()
);

-- =========================================================
-- 3) Trip System (with snapshots)
-- =========================================================

CREATE TABLE IF NOT EXISTS public.trips (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  shipper_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE RESTRICT,
  consignee_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE RESTRICT,
  carrier_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  stack_id uuid REFERENCES public.stacks(id) ON DELETE SET NULL,

  status trip_status NOT NULL DEFAULT 'pending',

  created_at timestamptz NOT NULL DEFAULT now(),
  started_at timestamptz,
  completed_at timestamptz,

  -- Snapshots (historical accuracy)
  origin_address_snapshot text NOT NULL,
  origin_coords_snapshot geography(POINT, 4326),
  destination_address_snapshot text NOT NULL,
  destination_coords_snapshot geography(POINT, 4326)
);

-- =========================================================
-- 4) Data & Analytics

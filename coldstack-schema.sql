-- =========================================================
-- Profile auto-creation on Auth signup
-- =========================================================

-- Creates a profile row when a user signs up via Supabase Auth.
-- Role defaults to 'shipper' (you can change it later via app logic).
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, auth
AS $$
BEGIN
  INSERT INTO public.profiles (id, full_name, role, email, phone, onboarding_completed, created_at)
  SELECT
    NEW.id,
    COALESCE(NEW.raw_user_meta_data->>'full_name', NULL),
    COALESCE((NEW.raw_user_meta_data->>'role')::user_role, 'shipper')::user_role,
    NEW.email,
    COALESCE(NEW.raw_user_meta_data->>'phone', NULL),
    FALSE,
    now()
  WHERE NOT EXISTS (
    SELECT 1 FROM public.profiles p WHERE p.id = NEW.id
  );

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
AFTER INSERT ON auth.users
FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

